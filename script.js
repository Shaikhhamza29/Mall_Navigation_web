function nearest_node(point, store) {

    let nodes = Object.keys(store).filter(
        n => n.startsWith("P") || n.startsWith("Esc")
    );

    let best = nodes[0];
    let minDist = distance(store[best], point);

    for (let n of nodes) {
        let d = distance(store[n], point);
        if (d < minDist) {
            minDist = d;
            best = n;
        }
    }

    return best;
}

