function getFloorPath(from, to) {

    const floors = ["GF", "F1", "F2", "F3"];

    const start = floors.indexOf(from);
    const end = floors.indexOf(to);

    if (start === -1 || end === -1) {
        return `${from} → ${to}`;
    }

    const step = start < end ? 1 : -1;

    let path = [];

    for (let i = start; i !== end + step; i += step) {
        path.push(floors[i]);
    }

    return path.join(" → ");
}


