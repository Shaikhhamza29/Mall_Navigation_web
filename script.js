function make_bidirectional(graph) {
    for (let node of Object.keys(graph)) {
        for (let neighbor of graph[node]) {

            if (!(neighbor in graph)) {
                graph[neighbor] = [];
            }

            if (!graph[neighbor].includes(node)) {
                graph[neighbor].push(node);
            }
        }
    }
    return graph;
}

