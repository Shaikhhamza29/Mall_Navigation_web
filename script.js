function astar(graph, start, end, store) {

    let open_set = [];
    open_set.push([0, start]);

    let came_from = {};

    let g_score = {};
    let f_score = {};

    for (let node in graph) {
        g_score[node] = Infinity;
        f_score[node] = Infinity;
    }

    g_score[start] = 0;
    f_score[start] = distance(store[start], store[end]);

    while (open_set.length > 0) {

        open_set.sort((a, b) => a[0] - b[0]);
        let current = open_set.shift()[1];

        if (current === end) {
            let path = [];
            while (current in came_from) {
                path.push(current);
                current = came_from[current];
            }
            path.push(start);
            return path.reverse();
        }

        let neighbors = graph[current] || [];

        for (let neighbor of neighbors) {

            let tentative_g =
                g_score[current] +
                distance(store[current], store[neighbor]);

            if (tentative_g < g_score[neighbor]) {

                came_from[neighbor] = current;
                g_score[neighbor] = tentative_g;

                f_score[neighbor] =
                    tentative_g +
                    distance(store[neighbor], store[end]);

                open_set.push([f_score[neighbor], neighbor]);
            }
        }
    }

    return [];
}

