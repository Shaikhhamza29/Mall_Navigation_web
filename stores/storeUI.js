function cleanStores(obj, floor) {
    return Object.keys(obj)
        .filter(name => !name.startsWith("P") && !name.startsWith("Esc"))
        .map(name => {

            // 🔥 convert name → filename
            let fileName = name
                .replace(/&/g, "")
                .replace(/\./g, "")
                .replace(/\s+/g, "_")
                .replace(/[^a-zA-Z0-9_]/g, "");

           return {
                name,
                floor,
                logo: `logos/${fileName}.png`,

                desc: storeDetails[name]?.desc || "No description available",
                offer: storeDetails[name]?.offer || "",
                timing: storeDetails[name]?.timing || "10 AM - 10 PM"
            };
            
        });
}

const stores = [
    ...cleanStores(stores_GF, "GF"),
    ...cleanStores(stores_F1, "F1"),
    ...cleanStores(stores_F2, "F2"),
    ...cleanStores(stores_F3, "F3")
];


// -----------------------------
// CREATE CARDS
// -----------------------------
function createCards(data) {

    const grid = document.getElementById("storeGrid");
    grid.innerHTML = "";

    data.forEach(store => {

        const card = document.createElement("div");
        card.className = "card";

        const img = document.createElement("img");
        img.src = store.logo;
        img.onerror = () => {
                img.onerror = null; // 🔥 prevents loop
                img.src = "logos/default.png";
            };

        const name = document.createElement("h3");
        name.innerText = store.name;

        const floor = document.createElement("p");
        floor.innerText = "Floor: " + store.floor;

        card.appendChild(img);
        card.appendChild(name);
        card.appendChild(floor);

        grid.appendChild(card);
        card.onclick = () => openPopup(store);
    });
}


// -----------------------------
// SEARCH
// -----------------------------
function searchStore() {

    const query = document.getElementById("searchInput").value.toLowerCase();

    const filtered = stores.filter(s =>
        s.name.toLowerCase().includes(query)
    );

    createCards(filtered);
}


// -----------------------------
// LIVE SEARCH + SUGGESTIONS
// -----------------------------
const input = document.getElementById("searchInput");
const suggestionBox = document.getElementById("suggestions");

let timer;

input.addEventListener("keyup", () => {

    clearTimeout(timer);

    timer = setTimeout(() => {

        const query = input.value.toLowerCase();

        if (!query) {
            suggestionBox.style.display = "none";
            createCards(stores);
            return;
        }

        const filtered = stores.filter(s =>
            s.name.toLowerCase().includes(query)
        );

        createCards(filtered);

    }, 200); // 🔥 delay
});


